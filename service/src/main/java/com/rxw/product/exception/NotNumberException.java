package com.rxw.product.exception;

public class NotNumberException extends Exception{

	public NotNumberException() {
		super();

	}

	public NotNumberException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public NotNumberException(String arg0) {
		super(arg0);

	}

	public NotNumberException(Throwable arg0) {
		super(arg0);

	}

}
