package com.reactiveworks.stocktrade.dao.exception;

public class StockTradeDAOException extends Exception {

	public StockTradeDAOException() {
		super();
		
	}

	public StockTradeDAOException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public StockTradeDAOException(String message) {
		super(message);
		
	}

	public StockTradeDAOException(Throwable cause) {
		super(cause);
		
	}
 
}
